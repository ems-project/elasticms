<?php

namespace EMS\CommonBundle\Common;

class Converter
{
    public static function toAscii(string $str): string
    {
        $clean = Converter::convertSpecialChars($str);
        try {
            $clean = \iconv('UTF-8', 'ASCII//TRANSLIT', $clean);
        } catch (\Exception $e) {
            $clean = false;
        }

        if (false === $clean) {
            $clean = Converter::convertSpecialChars($str);
        }

        $clean = (string) \preg_replace("/[^a-zA-Z0-9\_\|\ \-\.]/", '', $clean);
        $clean = \strtolower(\trim($clean, '-'));
        $clean = (string) \preg_replace("/[\/\_\|\ \-]+/", '-', $clean);

        return $clean;
    }

    public static function formatBytes(int $bytes, int $precision = 2): string
    {
        $units = ['B', 'KB', 'MB', 'GB', 'TB'];
        $bytes = \max($bytes, 0);

        $pow = \floor(($bytes ? \log($bytes) : 0) / \log(1024));
        $pow = \min($pow, \count($units) - 1);

        $bytes /= 1024 ** $pow;

        return \round($bytes, $precision).' '.$units[$pow];
    }

    /**
     * @param mixed $var
     */
    public static function stringify($var, string $defaultValue = ''): string
    {
        if (!\is_array($var) && !\is_object($var)) {
            return \strval($var);
        }
        if (\is_object($var) && \method_exists($var, 'toString')) {
            return $var->toString();
        }
        try {
            return \json_encode($var, JSON_THROW_ON_ERROR);
        } catch (\Throwable $e) {
            return $defaultValue;
        }
    }

